'''
Задача 3. Число наоборот
Пользователь вводит два числа: N и K. Напишите программу, которая заменяет
каждое число на число, которое получается из исходного записью его цифр в
обратном порядке, затем складывает их, снова переворачивает и выводит
ответ на экран.
Пример:
Введите первое число: 102
Введите второе число: 123
Первое число наоборот: 201
Второе число наоборот: 321
Сумма: 522
Сумма наоборот: 225
Подсказка № 1
Функция reversal(x) должна обрабатывать каждую цифру числа, начиная с
последней и добавляя её в новое число. Преобразование можно сделать, используя
остаток от деления и целочисленное деление.
Подсказка № 2
В функции reversal(x) переменная count отслеживает количество цифр, а
revers_x хранит перевёрнутое число. Не забудьте обновлять revers_x на каждой
итерации.
Подсказка № 3
После ввода чисел и их переворачивания с помощью функции reversal, сложите
перевёрнутые числа. Затем примените reversal к результату суммы, чтобы получить
окончательный результат.
'''
'''
1. Пользователь вводит два числа: ( N ) и ( K ).
2. Программа должна перевернуть каждое из этих чисел.
3. Затем программа складывает перевёрнутые числа.
4. Полученную сумму также нужно перевернуть и вывести на экран.

Пример
•  Ввод: ( N = 102 ), ( K = 123 )

•  Перевёрнутые числа: ( 201 ) и ( 321 )

•  Сумма перевёрнутых чисел: ( 201 + 321 = 522 )

•  Перевёрнутая сумма: ( 225 )

Подсказки
1. Функция reversal(x): Эта функция должна переворачивать число. Для этого можно использовать остаток от деления и целочисленное деление.
2. Переменные count и revers_x: Переменная count отслеживает количество цифр, а revers_x хранит перевёрнутое число. Обновляйте revers_x на каждой итерации.
3. Основная программа: После ввода чисел и их переворачивания сложите перевёрнутые числа. Затем примените reversal к результату суммы.
'''

def reversal(x):
   reversed_x = 0
   while x > 0:
      reversed_x = reversed_x * 10 + x % 10
      x //= 10
   return reversed_x

def main():
   N = int(input("Введите первое число: "))
   K = int(input("Введите второе число: "))

   reversed_N = reversal(N)
   reversed_K = reversal(K)

   print("Первое число наоборот:", reversed_N)
   print("Второе число наоборот:", reversed_K)

   sum_reversed = reversed_N + reversed_K
   print("Сумма:", sum_reversed)

   final_result = reversal(sum_reversed)
   print("Сумма наоборот:", final_result)

if __name__ == "__main__":
   main()