def is_valid_queen_placement(queens):
   def is_under_attack(x, y, queens):
      for qx, qy in queens:
         if x == qx or y == qy or abs(x - qx) == abs(y - qy):
            return True
      return False

# Проверяем, что все ферзи находятся в уникальных строках и столбцах
   rows = set()
   cols = set()
   for x, y in queens:
      if x in rows or y in cols:
         return False
   rows.add(x)
   cols.add(y)

# Проверяем, что ферзи не находятся на одной диагонали
   for i in range(len(queens)):
      for j in range(i + 1, len(queens)):
         if abs(queens[i][0] - queens[j][0]) == abs(queens[i][1] - queens[j][1]):
            return False

      return True

# Пример использования функции
