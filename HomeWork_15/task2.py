'''
 Задача2.Работа с текущим временем и датой
 Напишите скрипт,который получает текущее время и дату,азатем выводит их в
 формате YYYY-MM-DDHH:MM:SS.Дополнительно, выведите день недел и иномер
 недели в году.
Подсказка№1
 Используйте fromdatetimeimportdatetime,чтобы получить доступ к текущему
 времени и дате,а также к методам для их форматирования.
 Подсказка№2
 Используйте datetime.now() для получения объекта datetime, содержащего
 текущее время и дату.
 Подсказка№3
 Примените метод strftime() для форматирования даты и времени в строку с
 нужным форматом,например,'%Y-%m-%d%H:%M:%S'.
 Подсказка№4
 Используйте strftime('%A') для получения полного названия дня недели.
 Используйте isocalendar()[1] для получения номера недели в году
'''
from datetime import datetime

# Получаем текущее время и дату
now = datetime.now()

# Форматируем дату и время
formatted_datetime = now.strftime('%Y-%m-%d %H:%M:%S')

# Получаем день недели и номер недели в году
day_of_week = now.strftime('%A')
week_number = now.isocalendar()[1]

# Выводим результаты
print(f"Текущая дата и время: {formatted_datetime}")
print(f"День недели: {day_of_week}")
print(f"Номер недели в году: {week_number}")

# Этот скрипт выведет текущую дату и время в формате YYYY-MM-DD HH:MM:SS, а также день недели и номер недели в году. 